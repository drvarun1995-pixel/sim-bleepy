async function getCertificateStats() {
  try {
    // Get total certificates generated
    const { count: totalCertificates } = await supabaseAdmin
      .from('certificates')
      .select('*', { count: 'exact', head: true })

    // Get total templates (active ones)
    const { count: totalTemplates } = await supabaseAdmin
      .from('certificate_templates')
      .select('*', { count: 'exact', head: true })

    // Get events with certificates
    const { count: eventsWithCertificates } = await supabaseAdmin
      .from('certificates')
      .select('event_id', { count: 'exact', head: true })

    return {
      totalCertificates: totalCertificates || 0,
      totalTemplates: totalTemplates || 0,
      eventsWithCertificates: eventsWithCertificates || 0
    }
  } catch (error) {
    console.error('Error fetching certificate stats:', error)
    return {
      totalCertificates: 0,
      totalTemplates: 0,
      eventsWithCertificates: 0
    }
  }
}

async function getRecentActivity(userId: string) {
  try {
    // Get recent certificates generated by the user
    const { data: recentCertificates } = await supabaseAdmin
      .from('certificates')
      .select(`
        id,
        generated_at,
        event_id,
        events!inner(title)
      `)
      .eq('generated_by', userId)
      .order('generated_at', { ascending: false })
      .limit(3)

    // Get recent templates created by the user
    const { data: recentTemplates } = await supabaseAdmin
      .from('certificate_templates')
      .select('id, name, created_at')
      .eq('created_by', userId)
      .order('created_at', { ascending: false })
      .limit(3)

    return {
      recentCertificates: recentCertificates || [],
      recentTemplates: recentTemplates || []
    }
  } catch (error) {
    console.error('Error fetching recent activity:', error)
    return {
      recentCertificates: [],
      recentTemplates: []
    }
  }
}

async function getSharedTemplates(userId: string) {
  try {
    // Get templates shared by others (not created by current user)
    const { data: sharedTemplates } = await supabaseAdmin
      .from('certificate_templates')
      .select(`
        id,
        name,
        created_at,
        shared_at,
        created_by,
        users!certificate_templates_created_by_fkey(name)
      `)
      .eq('is_shared', true)
      .neq('created_by', userId)
      .order('shared_at', { ascending: false })
      .limit(6)

    return sharedTemplates || []
  } catch (error) {
    console.error('Error fetching shared templates:', error)
    return []
  }
}

async function getFeaturedTemplates() {
  try {
    // Get most popular templates (most used certificates)
    const { data: featuredTemplates } = await supabaseAdmin
      .from('certificate_templates')
      .select(`
        id,
        name,
        created_at,
        is_shared,
        users!certificate_templates_created_by_fkey(name)
      `)
      .eq('is_shared', true)
      .order('created_at', { ascending: false })
      .limit(3)

    return featuredTemplates || []
  } catch (error) {
    console.error('Error fetching featured templates:', error)
    return []
  }
}

